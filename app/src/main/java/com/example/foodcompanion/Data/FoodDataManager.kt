package com.example.foodcompanion.data

import com.google.gson.Gson

class FoodDataManager {
    private val gson = Gson()

    fun fromJson(json: String): FoodData.FoodData {
        return gson.fromJson(json, FoodData.FoodData::class.java)
    }

    fun toJson(foodData: FoodData.FoodData): String {
        return gson.toJson(foodData)
    }
}


